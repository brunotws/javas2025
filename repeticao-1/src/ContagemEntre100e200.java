public class ContagemEntre100e200 {
    public static void main(String[] args) {
        // Declaração da variável cont
        int cont = 101;  // Inicia com 101 para começar a contagem após 100

        // Laço "enquanto" (while) para imprimir os números entre 100 e 200
        while (cont < 200) {
            System.out.println("Contagem: " + cont);
            cont = cont + 1; // Incrementa o contador em 1
        }
    }
}
